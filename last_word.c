#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] != 32 && argv[1][i] != 9)
			{
				putchar(argv[1][i]);
				i++;
			}
		}
	}
}
